static const DBEntry* findEntry(const uint32_t& id)
{
  static const size_t dbSize = sizeof(gamedb) / sizeof(gamedb[0]);

  for (size_t i = 0; i < dbSize; ++i)
    if (gamedb[i].id == id) return &gamedb[i];

  return nullptr;
}

static uint32_t getSaveSize(const int32_t& saveType)
{
  switch (saveType)
  {
    case 1:  return 0x000200; // EEPROM 512B
    case 2:  return 0x002000; // EEPROM 8KB
    case 3:  return 0x010000; // EEPROM 64KB
    case 4:  return 0x020000; // EEPROM 128KB
    case 5:  return 0x040000; // FLASH 256KB
    case 6:  return 0x080000; // FLASH 512KB
    case 7:  return 0x100000; // FLASH 1024KB
    case 8:  return 0x800000; // FLASH 8192KB
    default: return 0x000000;
  }
}

GameInfo GameDB::analyze(const uint32_t& romCode)
{
  GameInfo game = {};
  game.id = romCode;

  const DBEntry* entry = findEntry(romCode);
  if (entry) game.saveType = entry->saveType;

  game.saveSize = getSaveSize(game.saveType);

  return game;
}
